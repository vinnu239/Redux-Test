import { render, screen } from '@testing-library/react';
import Async from './Async';

describe('Async component', () => {
  test('renders posts if request succeeds', async () => {
    // test is javaScript testing tool internally ir performs mocks which
    // avoids the dupilcation or potential errors while testing the url
    // if without using the jest we rperform the test than unncessarily it creates network trffic and duplicate data will add in to the db
    window.fetch = jest.fn();
    window.fetch.mockResolvedValueOnce({
      json: async () => [{ id: 'p1', title: 'First post' }],
    });
    render(<Async />);

    const listItemElements = await screen.findAllByRole('listitem');
    expect(listItemElements).not.toHaveLength(0);
  });
});